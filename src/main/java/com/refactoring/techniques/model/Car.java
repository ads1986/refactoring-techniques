package com.refactoring.techniques.model;

public class Car {
    private String name;
    private String brand;
    private int year;
    private double price;
    private double discount;

    public String getName() {
        return name;
    }

    public String getBrand() {
        return brand;
    }

    public int getYear() {
        return year;
    }

    public double getPrice() {
        return price;
    }

    public double getDiscount() {
        return discount;
    }
}
